void displayOptions() {
  print("-Options-");
  print("[1] Add");
  print("[2] Subtract");
  print("[3] Multiply");
  print("[4] Divide");
  print("[5] Exit");
  print("Please choose option");
}

double add(double num1, double num2) {
  return num1 + num2;
}

double subtract(double num1, double num2) {
  return num1 - num2;
}

double multiply(double num1, double num2) {
  return num1 * num2;
}

double divide(double num1, double num2) {
  return num1 / num2;
}
